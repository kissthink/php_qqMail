function huifuyoujian()
{
	var email_info = {};
	email_info.to = $('#to').val().replace(/(^\s*)|(\s*$)/g, ""); 
	if(email_info.to == '')
	{
		sys_alert('请输入收件人');
		return false;
	}
	if(!checkEmail(email_info.to))
	{
		sys_alert('请输入格式正确的收件人邮箱');
		return false;
	}
	email_info.subject = $('#subject').val().replace(/(^\s*)|(\s*$)/g, ""); 
	if(email_info.subject == '')
	{
		sys_alert('请输入收件人');
		return false;
	}
	email_info.content = $('#content').val().replace(/(^\s*)|(\s*$)/g, ""); 
	if(email_info.content == '')
	{
		sys_alert('请输入邮件内容');
		return false;
	}
	sys_alert('邮件发送中，请稍后……');
	$.ajax({
	 type: "POST",
	 url: "/email/send",
	 data: email_info,
	 error: function (XMLHttpRequest, textStatus, errorThrown) {
	 },
	 success: function(data){
							sys_alert(data);
						}
	});
}

function checkEmail(str)
{
	var re = /^(\w-*\.*)+@(\w-?)+(\.\w{2,})+$/;
	if(re.test(str))
	{
		return true;
	}
	else
	{
		return false;
	}
}

function sys_alert(mes)
{
	$('#tsxx').html(mes);
	$('#myModal').modal('show');	
}
